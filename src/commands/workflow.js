    console.log(chalk.green('\n‚úÖ GitHub Actions Workflow Generated\n'));
    console.log(chalk.yellow('Required GitHub Secrets:'));
    console.log(chalk.gray('  ‚Ä¢ SSH_PRIVATE_KEY - SSH key for server access'));
